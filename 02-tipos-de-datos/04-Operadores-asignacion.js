// Nos permite asignar un valor a las variable declaradas

let a = 1; //La viriable contiene el valor de 1

//Asignación compuesto
a += 3; // Esta seria similar al siguiente: a = a + 3
console.log(a)

a -= 2; // Expresion similiar: a = a - 2;
console.log(a);


